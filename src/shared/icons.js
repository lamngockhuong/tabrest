/**
 * Generate SVG element string
 * @param {string} name - Icon name from ICONS
 * @param {number} size - Icon size in pixels (default: 16)
 * @returns {string} SVG HTML string
 */
export function icon(name, size = 16) {
  const path = ICONS[name];
  if (!path) return "";
  return `<svg class="icon" width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round">${path}</svg>`;
}

/**
 * Inject icons into elements with data-icon attribute
 * Call after rendering dynamic content to replace placeholders
 */
export function injectIcons() {
  document.querySelectorAll("[data-icon]").forEach((el) => {
    const name = el.dataset.icon;
    const size = Number.parseInt(el.dataset.iconSize || "14", 10);
    el.innerHTML = icon(name, size);
  });
}
